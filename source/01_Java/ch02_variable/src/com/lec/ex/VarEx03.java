package com.lec.ex;
//변수 선언방법
public class VarEx03 {
	public static void main(String[] args) {
		int num1 = 10;
		//자료형 변수
		int num2;
		num2 = 20;
		
		System.out.printf("num1=%d \t num1=%d \n", num1, num2);
		//자료형 변수1=값1, 변수2=값2, 동일자료형변수를 다수 선언과 초기화
		int num3 = 30, num4 = 40;
		System.out.printf("num3=%d \t num4=%d \n", num3, num4);
		//자료형 변수1, 변수2..
		int num5, num6;
		num5 = 50; num6 = 60;
	}

}
